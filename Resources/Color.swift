// Color Extensions (Extensions/Color+Extensions.swift)
import SwiftUI

// The Color extension defines custom colors.
extension Color {
    // Earth-themed light color.
    static let EarthLight = Color("EarthLight")
    
    // Earth-themed dark color.
    static let EarthDark = Color("EarthDark")
    
    // Earth-themed accent color.
    static let EarthAccent = Color("EarthAccent")
}
